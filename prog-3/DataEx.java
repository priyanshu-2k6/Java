public class DataEx {
    public static void main(String[] args) {

        byte day = 12;
        short counts = 4556;
        int income = 100000;
        long population = 7800000000L;
        float area = 14.45f;
        double pi = 3.141592653589;
        char letter = 'U';
        char unicode = '\u0075';
        boolean yes = false;

        System.out.println("Byte value: " + day);
        System.out.println("Short value: " + counts);
        System.out.println("Int value: " + income);
        System.out.println("Long value: " + population);
        System.out.println("Float value: " + area);
        System.out.println("Double value: " + pi);
        System.out.println("Char value: " + letter);
        System.out.println("Unicode Char value: " + unicode);
        System.out.println("Boolean value: " + yes);

    }
}